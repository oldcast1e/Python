s = float(input("당신의 학점을 입력하세요: "))
if s==4.5:
    print("신")
elif 4.2<=s:
    print("교수님의 사랑")
elif 3.5<=s:
    print("현 체제의 수호자")
elif 2.8<=s:
    print("일반인")  
elif 2.3<=s:
    print("일탈을 꿈꾸는 소시민")
elif 1.75<=s:
    print("오락문화의 선구자")
elif 1.0<=s:
    print("불가촉천민") 
elif 0.5<=s:
    print("자벌레")
elif 0<s:
    print("플랑크톤")
else:
    print("시대를 앞서가는 혁명의 씨앗")